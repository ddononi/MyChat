package kr.co.myChat;

/**
 * 친구정보를 담을 클래스
 */
public class Friend {
	private String id;
	private String time;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

}
